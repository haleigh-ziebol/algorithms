# comparison
## greedy- fast but may not alwasy yield optimal soln
## dynamic- guarantee optimal soln, can be more complex and computationally intensive
## divide and conquer- breaks down problem but may involve redundant calcs

## dynamic programming
### holistic! break into smaller subproblems, solves sub, stores solutions, considers global context, checks for existing solution

## divide and conquer
### divide into sub problems, solves each sub, combines solutions, focus on final solution

## greedy
### focus on individual steps, myopic view, seek immediate gain, straightforward and faster, no global consideration
