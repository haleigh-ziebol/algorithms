#overview
## used in: social networks, search engines, GPS nav, network connectivity and routing, recommendation systems

## types
### traversal: visits each vertice- depth first serch (DFS), breadth-first search (BFS)
### shortest path: shortest path between nodes- Dijkstra's alg, Bellman-Ford
### spanning tree: minimizes edges- Kruskal's, Prim's
### network flow: max flow prob- traffic mgmt, match resources to tasks


#DFS
## explore as deep into branch as possible before trying other route, all nodes visited
## explores all paths, memory constraints, deeply nested solutions

#BFS
## first in first out
## process nodes in order they are discovered
## shortest path (unweighted), nearest point of interest, peer to peer networks



